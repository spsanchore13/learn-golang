package main

import (
	"fmt"
)

func main() {

	var firstName string = "Shantilal"
	var lastName = "Patliya"

	fmt.Println(firstName)
	fmt.Println(lastName)

	var age int = 26
	var phoneNumber = 9784102014

	fmt.Println(age)
	fmt.Println(phoneNumber)

	var height float64 = 5.6

	fmt.Println(height)

	var isMarried bool = false

	fmt.Println(isMarried)

	const pi = 45

	fmt.Println(pi)

	// shortcut to declare variable

	person := 123
	fmt.Println(person)

	var Public = "data is important"
	var private = "data is private"

	fmt.Println(Public)
	fmt.Println(private)

}
